package aula1;

public class Calculadora {

    int soma(int valorUm, int valorDois) {
        int resultado = valorUm + valorDois;
        return resultado;
    }
    int subtrai (int valorUm, int valorDois) {
        int resultado = valorUm - valorDois;
        return resultado;
    }

        int multiplica (int valorUm, int valorDois) {
            int resultado = valorUm * valorDois;
            return resultado;
    }
}
